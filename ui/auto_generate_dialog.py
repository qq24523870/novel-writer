    def start_generation(self):
        if self._generating:
            return

        is_continuation = self._selected_project_id > 0

        if is_continuation:
            project_info = project_manager.get_project(self._selected_project_id)
            if not project_info:
                QMessageBox.warning(self, "提示", "所选项目不存在，请重新选择")
                return
            novel_name = project_info["name"]
            male_name = self._male_name.text().strip()
            female_name = self._female_name.text().strip()
            novel_type = project_info.get("genre", "")
            protagonist = ""
            setting = ""
            plot = project_info.get("description", "")
            outline = ""
            master_outline = ""
            context_type = "continuation"
            use_outline_mode = False

            from_index = self._chapter_from.currentData() or 0
            to_index = self._chapter_to.currentData() or 0
            if from_index <= 0:
                QMessageBox.warning(self, "提示", "请选择有效的起始章节")
                return

            chapters_to_generate = list(range(from_index, to_index + 1))
            gen_chapters = len(chapters_to_generate)

            existing_text_parts = []
            existing_chapters = self._existing_chapters_cache
            for ch in existing_chapters:
                content = ch.get("content", "")
                if content:
                    opening = content[:100].replace("\n", " ")
                    closing = content[-200:].replace("\n", " ") if len(content) > 200 else content.replace("\n", " ")
                    existing_text_parts.append(
                        f"【{ch['title']}】{opening}...【结尾】{closing}"
                    )
            existing_project_context = "\n\n".join(existing_text_parts)
        else:
            novel_name = self._novel_name.text().strip()
            if not novel_name:
                QMessageBox.information(self, "提示", "请填写小说名称")
                self._novel_name.setFocus()
                return

            male_name = self._male_name.text().strip()
            female_name = self._female_name.text().strip()
            existing_project_context = ""

            master_outline = self._master_outline.toPlainText().strip()
            use_outline_mode = bool(master_outline)

            if use_outline_mode:
                novel_type = ""
                protagonist = ""
                setting = ""
                plot = ""
                outline = ""
            else:
                novel_type = self._novel_type.currentText().strip()
                protagonist = self._protagonist.toPlainText().strip()
                setting = self._setting.toPlainText().strip()
                plot = self._plot.toPlainText().strip()
                outline = self._outline.toPlainText().strip()

                if not all([novel_type, protagonist, plot]):
                    QMessageBox.warning(self, "提示",
                        "请至少填写小说类型、主角设定和核心剧情。\n"
                        "或者在上方填写「总大纲」直接生成。")
                    return

            gen_chapters = config_manager.get("writing.generate_chapter_count", 10)

        gen_words = config_manager.get("writing.generate_word_per_chapter", 2000)

        self._switch_to_generating_mode()

        context_type = "outline" if use_outline_mode else ("continuation" if is_continuation else "detail")

        raw_style = self._writing_style.currentText()
        raw_style_name = raw_style.split(" - ", 1)[0]
        if raw_style == "（默认）":
            writing_style_text = ""
        elif raw_style_name == "自动文笔":
            writing_style_text = "请根据故事大纲和主题内容，自动选择最适合的文笔风格进行创作（如热血类用简洁有力、言情类用细腻优美、悬疑类用冷峻紧张等）。"
        else:
            style_part = raw_style.split(" - ", 1)
            style_name = style_part[0]
            style_desc = style_part[1] if len(style_part) > 1 else ""
            writing_style_text = f"采用{style_name}文笔风格。{style_desc}"

        context_data = {
            "master_outline": master_outline,
            "novel_type": novel_type,
            "protagonist": protagonist,
            "setting": setting,
            "plot": plot,
            "outline": outline,
            "male_name": male_name,
            "female_name": female_name,
            "writing_style": writing_style_text,
            "gender": self._gender.currentText(),
            "novel_name": novel_name,
            "existing_project_context": existing_project_context,
        }

        self._thread = threading.Thread(
            target=self._generation_worker,
            args=(novel_name, novel_type, context_type, context_data,
                  gen_chapters, gen_words, male_name, female_name,
                  is_continuation),
            daemon=True
        )
        self._thread.start()

