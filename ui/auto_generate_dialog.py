    def _load_existing_projects(self):
        """加载已有项目列表"""
        self._existing_project.clear()
        self._existing_project.addItem("（不续写，新建小说）", 0)
        try:
            projects = project_manager.get_all_projects()
            for p in projects:
                name = p.get("name", f"项目ID:{p['id']}")
                self._existing_project.addItem(f"{name}", p["id"])
        except Exception as e:
            logger.error(f"加载项目列表失败: {e}")

    def _on_existing_project_changed(self, idx: int):
        """已有项目选择变更"""
        pid = self._existing_project.currentData()
        self._selected_project_id = pid if pid else 0

        # 清除续写模式标签
        self._continue_mode_label.setText("")

        if self._selected_project_id:
            # 加载该项目的章节信息
            from core.writing_core import chapter_manager
            chapters = chapter_manager.get_all_chapters_for_project(self._selected_project_id)
            self._existing_chapters_cache = chapters
            total = len(chapters)

            self._chapter_from.clear()
            self._chapter_to.clear()
            for i, ch in enumerate(chapters, 1):
                label = f"{i} - {ch['title']}"
                self._chapter_from.addItem(label, i)
                self._chapter_to.addItem(label, i)

            if total > 0:
                self._chapter_from.setCurrentIndex(0)
                self._chapter_to.setCurrentIndex(total - 1)

            self._continue_mode_label.setText(
                f"🔗 续写模式已启用：当前共{total}章，将从第{total + 1}章开始续写"
            )
        else:
            self._existing_chapters_cache = []
            self._chapter_from.clear()
            self._chapter_to.clear()

